    private void jComboBox4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox4ActionPerformed
        if(jComboBox4.getSelectedItem()==null)
        {
            return;
        }
        branch.clear();
        String s=(String)jComboBox4.getSelectedItem();
        int courseId=course.get(s);
        String query="select * from branch where courseId=?";
        Object ob[]=new Object[1];
        ob[0]=courseId;
        ResultSet rs=dbaccess.selectquery(query, ob);
        try
        {
            jComboBox1.removeAllItems();
            while(rs.next())
            {

                int a=rs.getInt(1);
                String b=rs.getString(2);
                branch.put(b, a);
                jComboBox1.addItem(b);
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jComboBox4ActionPerformed

