        {
            JOptionPane.showMessageDialog(null, "Enter Roll Number to continue");
            return;
        }
        int roll=Integer.parseInt(jTextField1.getText());
        int semId=sem.get(jComboBox3.getSelectedItem());
        int branchId=branch.get(jComboBox1.getSelectedItem());
        int sessId=session.get(jComboBox2.getSelectedItem());
