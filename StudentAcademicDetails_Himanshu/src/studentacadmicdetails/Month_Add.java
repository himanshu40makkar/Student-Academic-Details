    void fill()
    {
        jList1.setVisible(false);
        
        DefaultListModel dm=(DefaultListModel)jList1.getModel();
        String query="select * from month";
        ResultSet rs=dbaccess.selectquery(query, new Object[0]);
        dm.removeAllElements();
        try
        {
        while(rs.next())
        {
            dm.addElement(rs.getString(2));
        }}
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

