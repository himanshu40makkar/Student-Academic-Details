    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
try
{
    if(jList1.getSelectedValue()==null)
    {
        JOptionPane.showMessageDialog(null, "Select any month to Delete");
        return;
    }
    Object ob[]=new Object[1];
    ob[0]=jList1.getSelectedValue();
    String Query="delete from month where monthName=?";
    if(dbaccess.updatequery(Query, ob))
    {
        JOptionPane.showMessageDialog(null, "Month Deleted");
    }
    else
        JOptionPane.showMessageDialog(null, "Try Again");
    
    fill();
}
catch(Exception ex)
{
    JOptionPane.showMessageDialog(null, ex);
}
    }//GEN-LAST:event_jButton6ActionPerformed

