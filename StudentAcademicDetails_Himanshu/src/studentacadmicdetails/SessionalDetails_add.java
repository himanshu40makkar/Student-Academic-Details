    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
try
{
    String query="select * from sessionaldetail where sessionaldate=? and sessionalId=?";
    Object ob[]=new Object[2];
    ob[0]=jTextField1.getText();
    ob[1]=sessional.get(jComboBox1.getSelectedItem());
    ResultSet rs=dbaccess.selectquery(query, ob);
    DefaultTableModel tm=(DefaultTableModel)jTable1.getModel();
    tm.setRowCount(0);
    while(rs.next())
    {
        Object o[]=new Object[2];
        o[0]=sess.get(rs.getInt(1));
        o[1]=rs.getObject(2);
        tm.addRow(o);
        jButton3.setEnabled(true);
    }
}
catch(Exception ex)
{
    JOptionPane.showMessageDialog(null, ex);
}
        
    }//GEN-LAST:event_jButton2ActionPerformed

