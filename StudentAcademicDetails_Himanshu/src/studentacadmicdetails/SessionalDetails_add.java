    void fill()
{
    try
    {
        jButton3.setEnabled(false);
        String query="select * from sessional";
        ResultSet rs=dbaccess.selectquery(query, new Object[0]);
        jComboBox1.removeAllItems();
        while(rs.next())
        {
            int a=rs.getInt(1);
            String m=rs.getString(2);
            sessional.put(m, a);
            sess.put(a, m);
            jComboBox1.addItem(m);
        }
        DefaultTableModel dm=(DefaultTableModel)jTable1.getModel();
        dm.setRowCount(0);
        query="select * from sessionaldetail";
         rs=dbaccess.selectquery(query, new Object[0]);
         while(rs.next())
         {
            Object ob[]=new Object[2];
            ob[0]=rs.getObject(1);
            ob[1]=rs.getObject(2);
            dm.addRow(ob);
         }
    }
    catch(Exception ex)
    {
        JOptionPane.showMessageDialog(null, ex);
    }
}
