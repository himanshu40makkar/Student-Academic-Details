    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(jTextField1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter Branch Name");
            return;
        }
        Object ob[]=new Object[2];
        ob[0]=jTextField1.getText();
        ob[1]=tm.get(jComboBox1.getSelectedItem());
        String query="{call add_branch(?,?)}";
        if(dbaccess.updatequery(query, ob))
        {
            JOptionPane.showMessageDialog(null, "Branch Added");
            jTextField1.setText("");
            
            jButton5ActionPerformed(evt);
        
        }
        else
            JOptionPane.showMessageDialog(null, "try again");
    }//GEN-LAST:event_jButton2ActionPerformed

