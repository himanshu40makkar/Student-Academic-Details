    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
String query="select * from branch where courseId=?";
Object ob[]=new Object[1];
ob[0]=tm.get(jComboBox1.getSelectedItem());
dtm.setRowCount(0);
ResultSet rs=dbaccess.selectquery(query, ob);
try
{
while(rs.next())
{
    Object o[]=new Object[2];
    o[0]=jComboBox1.getSelectedItem();
    o[1]=rs.getString(2);
    dtm.addRow(o);
}}
catch(Exception ex)
{
    JOptionPane.showMessageDialog(null, ex);
}        // TODO add your handling code here:
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTable1.getSelectedRow()==-1)
        {
            JOptionPane.showMessageDialog(null,"Select any Record To delete");
            return;
        }
        int k=jTable1.getSelectedRow();
        Object ob[]=new Object[2];
        
        ob[1]=tm.get(jTable1.getValueAt( k,0));
            
        ob[0]=jTable1.getValueAt(k, 1);
        
        String query="{call del_branch(?,?)}";
        if(dbaccess.updatequery(query, ob))
        {
            JOptionPane.showMessageDialog(null, "Branch Deleted");
            jTextField1.setText("");
            fill();
            jButton5ActionPerformed(evt);
        
        }
        else
            JOptionPane.showMessageDialog(null,"Try Again");
        
    }//GEN-LAST:event_jButton4ActionPerformed

